from openerp import models, fields, api, _
import openerp.addons.decimal_precision as dp


class AccountInvoiceLine(models.Model) :

    _inherit = 'account.invoice.line'

    price_subtotal_taxed = fields.Float(string='PU HT', digits= dp.get_precision('Account'), store=True, readonly=True, compute='_compute_price')

    @api.one
    @api.depends('price_unit', 'discount', 'invoice_line_tax_ids', 'quantity',
                 'product_id', 'invoice_id.partner_id', 'invoice_id.currency_id', 'invoice_id.company_id',
                 'invoice_id.date_invoice', 'price_subtotal')
    def _compute_price(self):
        res = super(AccountInvoiceLine, self)._compute_price()
        currency = self.invoice_id and self.invoice_id.currency_id or None
        price = self.price_unit * (1 - (self.discount or 0.0) / 100.0)
        taxes = True
        if self.quantity > 0:
            taxes = self.invoice_line_tax_ids.compute_all(price, currency, self.quantity, product=self.product_id,
                                                          partner=self.invoice_id.partner_id)
        self.price_subtotal_taxed = self.price_subtotal / self.quantity